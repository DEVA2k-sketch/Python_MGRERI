numbers = [21, 34, 54, 12]
print("List",numbers)
#using Remove method
numbers.remove(34)
print("after remove",numbers)
#using insert method
numbers.insert(0,1)
print("after insertion:",numbers)
# using append method
numbers.append(32)
print("After Append:", numbers)
#using len method
print("length:",len(numbers))
#using pop method
numbers.pop(2)
print("after pop",numbers)
#using clear method
numbers.clear()
print("after clear method",numbers)
